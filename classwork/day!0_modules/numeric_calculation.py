#creating module for calulation 

#creating function for addition 
def calculate_add(num1 , num2):
    return(num1 + num2)

#creating function of subtraction
def calculate_sub(num1 , num2):
    return(num1 - num2)

#creating function for multiplication
def calculate_mul(num1 , num2):
    return(num1 * num2)

#creating function for division
def calculate_div(num1 , num2):
    return(num1 / num2)

#creating function for remainder
def calculate_rem(num1 , num2):
    return(num1 % num2)

